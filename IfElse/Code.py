age = 15

# 1. Simple 'if'
if age < 18:
    print("You are a minor.")

# 2. 'if-else'
if age >= 18:
    print("You can drive.")
else:
    print("You cannot drive yet.")

# 3. 'if-elif-else'
marks = 85
if marks >= 90:
    print("Grade: A")
elif marks >= 80:
    print("Grade: B")
else:
    print("Grade: C or below")